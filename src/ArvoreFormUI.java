    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
                // restringe a amostra a diretorios apenas
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int res = fc.showOpenDialog(null);
        if(res == JFileChooser.APPROVE_OPTION){
            File diretorio = fc.getSelectedFile();
            String path_dir = diretorio.getAbsolutePath();
            path_dir = path_dir + "\\arvore.txt";
            JOptionPane.showMessageDialog(null, "Voce escolheu o diretório: " + path_dir);
            SetPath(path_dir);
            CarregarArquivo();
        }
        else
            JOptionPane.showMessageDialog(null, "Voce nao selecionou nenhum diretorio.");
    }//GEN-LAST:event_jButton6ActionPerformed

