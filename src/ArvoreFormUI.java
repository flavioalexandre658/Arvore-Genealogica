    private void CarregarArquivo(){
        try{
            if(GetPath() != null){
                ArrayList<String> lista_string = new ArrayList();
                BufferedReader br = new BufferedReader(new FileReader(GetPath()));
                while(br.ready()){
                String linha = br.readLine();
                String string = new String();
                for(int cont = 0; cont < linha.length(); cont++){
                    char letra = linha.charAt(cont);
                    if(';' == letra){
                        lista_string.add(string);
                        string = new String();
                        try{
                            GetArvore().InserirNodo(lista_string.get(0), lista_string.get(1), lista_string.get(2), lista_string.get(3), lista_string.get(4), data_nasc.parse(lista_string.get(5)), null);
                        } catch (ParseException ex) {
                            Logger.getLogger(ArvoreFormUI.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        lista_string = new ArrayList();
                    }
                    if(',' != letra){
                        string +=letra;
                    }else{
                        lista_string.add(string);
                        string = new String();
                    }
                }
                }br.close();
            }
        }catch(IOException ioe){
            ioe.printStackTrace();
        }
    }
    
